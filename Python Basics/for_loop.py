# for loops
for items in 'Python':
    print(items)
print('............')
for nums in [1, 2, 3, 4, 5]:
    print(nums)
print('............')
for item in range(11):
    print(item)
print('............')
for num in range(0, 11):
    print(num)
print('............')
for number in range(0, 11, 2):
    print(number)
print('............')